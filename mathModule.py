import math
#min max, used for finding min and max values in given iterable
x = min(5, 10, 25)
y = max(5, 10, 25)
print(x)
print(y)
#he abs()
x = abs(-5.5)
print(x)
#The pow(x, y) function returns the value of x to the power of y (xy).
x = pow(4, 3)
print(x)
#square root 
x = math.sqrt(25)
print(x)
#The math.ceil() method rounds a number upwards to its nearest integer
# and the math.floor() method rounds a number downwards to its nearest integer
#returns the result after
x = math.ceil(1.4)
y = math.floor(1.4)
print(x) # returns 2
print(y) # returns 1

#pi in math
x = math.pi
print(x)